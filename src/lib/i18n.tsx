import React, { createContext, useContext, useState, useEffect, ReactNode } from 'react';

export type Language = 'en' | 'he' | 'ar' | 'fr' | 'ru';

export const LANGUAGES: { code: Language; label: string; nativeLabel: string; rtl: boolean }[] = [
  { code: 'ru', label: 'RU', nativeLabel: 'Русский', rtl: false },
  { code: 'en', label: 'EN', nativeLabel: 'English', rtl: false },
  { code: 'he', label: 'HE', nativeLabel: 'עברית', rtl: true },
  { code: 'ar', label: 'AR', nativeLabel: 'العربية', rtl: true },
  { code: 'fr', label: 'FR', nativeLabel: 'Français', rtl: false },
];

interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
  isRTL: boolean;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

const STORAGE_KEY = 'eveblue_language';
const LANG_VERSION_KEY = 'eveblue_lang_version';
const CURRENT_LANG_VERSION = '3'; // bump to reset all users to Hebrew

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    if (typeof window !== 'undefined') {
      const version = localStorage.getItem(LANG_VERSION_KEY);
      if (version === CURRENT_LANG_VERSION) {
        const stored = localStorage.getItem(STORAGE_KEY);
        if (stored && LANGUAGES.some(l => l.code === stored)) {
          return stored as Language;
        }
      }
      // Reset to Hebrew for users with old/no version
      localStorage.setItem(LANG_VERSION_KEY, CURRENT_LANG_VERSION);
      localStorage.setItem(STORAGE_KEY, 'he');
    }
    return 'he';
  });

  const isRTL = LANGUAGES.find(l => l.code === language)?.rtl ?? false;

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, language);
    
    // Update document direction for RTL languages
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.documentElement.lang = language;
  }, [language, isRTL]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
  };

  const t = (key: string): string => {
    const keys = key.split('.');
    let value: unknown = translations[language];
    
    for (const k of keys) {
      if (value && typeof value === 'object' && k in value) {
        value = (value as Record<string, unknown>)[k];
      } else {
        // Fallback to English
        value = translations.en;
        for (const fallbackKey of keys) {
          if (value && typeof value === 'object' && fallbackKey in value) {
            value = (value as Record<string, unknown>)[fallbackKey];
          } else {
            return key; // Return key if not found
          }
        }
        break;
      }
    }
    
    return typeof value === 'string' ? value : key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, isRTL }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}

